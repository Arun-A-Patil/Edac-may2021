class Swap
{
 public static void main (String [] args)
 {
  int a= 10,b=20,temp;
  
  System.out.println("a is equal to: "+ a +"," + "b is equal to: "+b);
  
  temp = a;
  a = b;
  b= temp;
  
  System.out.println("a is equal to: "+ a +"," + "b is equal to: "+b);
 }
 
}
  